package resources;

/**
 * A enumeração Area representa diferentes áreas médicas.
 *
 * Cada valor desta enumeração corresponde a uma área médica específica, como Pediatria, Cardiologia, etc.
 */
public enum Area {
    Geral, Pediatria, Cardiologia, Dermatologia, Ginecologia, Ortopedia, Oftalmologia, Neurologia, Psiquiatria,
    Odontologia, Reumatologia, Otorrinolaringologia, Oncologia, Hematologia, Alergologia, Infectologia
}
